def mobrand():                         #몬스터를 결정하기 위한 함수                                                                                                            #몬스터 결정 함수
    mobrand=random.randrange(1,101)    #랜덤 숫자 1~100 출력
    if mobrand<=48:                    #mobrand값이 ~ 일때 ~리턴
        return 1
    if 48 < mobrand <= 78:
        return 2
    if 78 < mobrand <= 90:
        return 3
    if 90 < mobrand <= 95:
        return 4
    if 95 < mobrand <= 97:
        return 5
    if 97 < mobrand <= 99:
        return 6
    if mobrand == 100:
        return 7

